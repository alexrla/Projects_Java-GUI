    public void decorateTableTask() {
        TableTasks.getTableHeader().setFont(new Font("Fira Code Retina", Font.BOLD, 18));

        TableTasks.getTableHeader().setBackground(new Color(98, 86, 130));

        TableTasks.getTableHeader().setForeground(new Color(255, 255, 255));

        // TableTasks.setAutoCreateRowSorter(true);

        TableTasks.getColumnModel().getColumn(2).setCellRenderer(new DeadlineStatus());

        TableTasks.getColumnModel().getColumn(4).setCellRenderer(new ButtonRenderer("edit"));

        TableTasks.getColumnModel().getColumn(5).setCellRenderer(new ButtonRenderer("delete"));
    }

    public void initDataController() {
        projectController = new ProjectController();
        taskController = new TaskController();
    }

    public void initComponentsModel() {
        projectsModel = new DefaultListModel();
        loadProjects();

        taskModel = new TaskTableModel();
        TableTasks.setModel(taskModel);

        if (!projectsModel.isEmpty()) {
            ListProjects.setSelectedIndex(0);

            Project project = (Project) projectsModel.get(0);

            loadTasks(project.getId());
        }
    }

    public void loadProjects() {
        List<Project> projects = projectController.getAll();

        projectsModel.clear();

        for (int i = 0; i < projects.size(); i++) {
            Project project = projects.get(i);

            projectsModel.addElement(project);
        }

        ListProjects.setModel(projectsModel);
    }

    public void loadTasks(int projectId) {
        List<Task> tasks = taskController.getAll(projectId);

        taskModel.setTasks(tasks);

        showTableTasks(!tasks.isEmpty());
    }

    private void showTableTasks(boolean hasTasks) {
        if (hasTasks) {
            if (NoTasksContainer.isVisible()) {
                NoTasksContainer.setVisible(false);
                Table.remove(NoTasksContainer);
            }

            Table.add(ScrollTable);
            ScrollTable.setVisible(true);
            ScrollTable.setSize(Table.getWidth(), Table.getHeight());
        } else {
            if (ScrollTable.isVisible()) {
                ScrollTable.setVisible(false);
                Table.remove(ScrollTable);
            }

            Table.add(NoTasksContainer);
            NoTasksContainer.setVisible(true);
            NoTasksContainer.setSize(Table.getWidth(), Table.getHeight());
        }
    }
