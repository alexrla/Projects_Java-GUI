    private void TasksBtnMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_TasksBtnMouseClicked
        // TODO add your handling code here:
        CreateTaskScreen screen = new CreateTaskScreen(this, rootPaneCheckingEnabled);

        // screen.setProject(null);

        int projectIndex = ListProjects.getSelectedIndex();

        Project project = (Project) projectsModel.get(projectIndex);

        screen.setProject(project);

        screen.setVisible(true);

        screen.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent evt) {
                int projectIndex = ListProjects.getSelectedIndex();

                Project project = (Project) projectsModel.get(projectIndex);

                loadTasks(project.getId());
            }
        });
    }// GEN-LAST:event_TasksBtnMouseClicked

